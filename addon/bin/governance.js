#!/usr/bin/env node
/**
 * Governance CLI — inspect and change the status of auto-detected components.
 *
 * Run from your project root (where `.storybook/` lives):
 *   node node_modules/storybook-addon-auto-detect/bin/governance.js list
 *   ... approve Button
 *   ... review Badge
 *   ... surface Card
 *   ... prune            # delete generated stories still 'surfaced'
 *
 * Status drives the sidebar group (Surfaced / In Review / Approved) on the next
 * Storybook start.
 */

import fs from 'node:fs';
import path from 'node:path';
import {
  loadManifest,
  saveManifest,
  setStatus,
  STATUSES,
} from '../src/governance.js';

const args = process.argv.slice(2);
let configDir = path.resolve(process.cwd(), '.storybook');
const positional = [];
for (let i = 0; i < args.length; i += 1) {
  if (args[i] === '--config') {
    configDir = path.resolve(args[i + 1] || '.storybook');
    i += 1; // skip the value
  } else {
    positional.push(args[i]);
  }
}
const [command, target] = positional;

const projectRoot = path.dirname(configDir);

function resolveKey(manifest, target) {
  if (manifest.components[target]) return target;
  // Match by component name (the part after "::").
  const keys = Object.keys(manifest.components).filter((k) => k.endsWith(`::${target}`));
  if (keys.length === 1) return keys[0];
  if (keys.length > 1) {
    console.error(`Ambiguous "${target}". Matches:\n  ${keys.join('\n  ')}`);
    process.exit(1);
  }
  return null;
}

function printList(manifest) {
  const entries = Object.entries(manifest.components);
  if (!entries.length) {
    console.log('No components tracked yet. Start Storybook once to populate the manifest.');
    return;
  }
  const width = Math.max(...entries.map(([k]) => k.length));
  for (const [key, entry] of entries) {
    const flag = entry.edited ? ' (hand-edited)' : '';
    console.log(`${key.padEnd(width)}  ${entry.status}${flag}`);
  }
}

function applyStatus(manifest, status) {
  if (!target) {
    console.error(`Usage: governance ${status} <ComponentName>`);
    process.exit(1);
  }
  const key = resolveKey(manifest, target);
  if (!key) {
    console.error(`No tracked component matches "${target}". Run "governance list".`);
    process.exit(1);
  }
  setStatus(manifest, key, status);
  saveManifest(configDir, manifest);
  console.log(`${key} -> ${status}`);
}

function prune(manifest) {
  let removed = 0;
  for (const [key, entry] of Object.entries(manifest.components)) {
    if (entry.status !== 'surfaced' || !entry.file) continue;
    const dir = path.dirname(path.resolve(projectRoot, entry.file));
    const base = path.basename(entry.file, path.extname(entry.file));
    for (const ext of ['jsx', 'tsx']) {
      const p = path.join(dir, `${base}.auto.stories.${ext}`);
      if (fs.existsSync(p) && fs.readFileSync(p, 'utf8').includes('Auto-generated by')) {
        fs.unlinkSync(p);
        entry.generatedHash = null;
        removed += 1;
        console.log(`pruned ${path.relative(projectRoot, p)}`);
      }
    }
  }
  saveManifest(configDir, manifest);
  console.log(`Pruned ${removed} surfaced story file(s).`);
}

function main() {
  const manifest = loadManifest(configDir);
  switch (command) {
    case 'list':
    case undefined:
      printList(manifest);
      break;
    case 'approve':
      applyStatus(manifest, 'approved');
      break;
    case 'review':
      applyStatus(manifest, 'in-review');
      break;
    case 'surface':
      applyStatus(manifest, 'surfaced');
      break;
    case 'set':
      applyStatus(manifest, positional[2]);
      break;
    case 'prune':
      prune(manifest);
      break;
    default:
      console.error(`Unknown command "${command}".`);
      console.error(`Commands: list | approve <C> | review <C> | surface <C> | set <C> <status> | prune`);
      console.error(`Statuses: ${STATUSES.join(', ')}`);
      process.exit(1);
  }
}

main();
